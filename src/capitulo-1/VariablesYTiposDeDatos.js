// Variables
var x = 1; // Declaracion de la variable
const y = 2; // Declaracion de una variable constante
let z = 3; // Equivalente a var, pero usar mejor esta

// Tambien se puede declarar una variable y asignarle
// el resultado de una operacion
const resultado = x + y + z;

// Herramienta para imprimir por consola y saber
// lo que esta pasando
console.log("El resultado de la operacion fue", resultado);

// Tipos de datos
// --- String o cadenas de texto
const nombreDeProducto = "patata";
// --- Number
const precio = 1.5;
// --- Boolean (verdadero o falso)
const stock = true;
// --- Object
const personaje = {
  nombre: "Persona Pajaro",
  edad: 40
};
// --- Arrays o Colecciones
const mediciones = [1.03, 1.1, 1.09, 1.13];

// Otros tipos de datos
console.log(undefined);
console.log(null);
console.log(NaN);
